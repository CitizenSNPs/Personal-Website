$(document).ready(function(){

$('.navbar a').mouseover(function(){
  $(this).css({'color':'#66fcf1'});
});

$('.navbar a').mouseout(function(){
  $(this).css({'color':'white'})
});

$('.social').mouseover(function(){
  $(this).css({'color':'#66fcf1'});
});

$('.social').mouseout(function(){
  $(this).css({'color':'white'})
});

});
